TextStyle commonstyle({double size=12,Color color=Colors.white,FontWeight weight=FontWeight.w400}){
  return GoogleFonts.poppins(
    fontSize: size,
    color: color,
    fontWeight: weight,
    letterSpacing: 0.21
  );
}
